#include "pushswap.h"

void	ft_pa(t_list *d)
{
	int	i;

	i = d->sia;
	d->sia = d->sia + 1;
	d->sib = d->sib - 1;
	while (i > 0)
	{
		ft_swap(&d->sa[i], &d->sa[i - 1]);
		i--;
	}
	d->sa[0] = d->sb[0];
	i = 0;
	while (i <= d->sib)
	{
		d->sb[i] = d->sb[i + 1];
		i++;
	}
	write(1, "pa\n", 3);
}
