#include "pushswap.h"

int	ft_check(char *arg)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (((arg[j] == '-' || arg[j] == '+') && (arg[j + 1] >= '0'
				&& arg[j + 1] <= '9')) || (arg[j] >= '0' && arg[j] <= '9'))
	{
		i++;
		j++;
		while (arg[j])
		{
			if (arg[j] >= '0' && arg[j] <= '9')
			{
				i++;
			}
			j++;
		}
	}
	if (i == ft_strlen(arg))
	{
		return (0);
	}
	return (-1);
}
