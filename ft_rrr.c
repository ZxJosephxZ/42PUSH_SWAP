#include "pushswap.h"

void	ft_rrr(t_list *d)
{
	int	i;

	i = d->sia - 1;
	while (i > 0)
	{
		ft_swap(&d->sa[i], &d->sa[i - 1]);
		i--;
	}
	i = d->sib - 1;
	while (i - 1 >= 0)
	{
		ft_swap(&d->sb[i], &d->sb[i - 1]);
		i--;
	}
	write(1, "rrr\n", 4);
}
