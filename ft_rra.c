#include "pushswap.h"

void	ft_rra(t_list *d)
{
	int	i;

	i = d->sia - 1;
	while (i > 0)
	{
		ft_swap(&d->sa[i], &d->sa[i - 1]);
		i--;
	}
	write(1, "rra\n", 4);
}
