#include "pushswap.h"

void	ft_ra(t_list *d)
{
	int	i;

	i = 0;
	while (i + 1 < d->sia)
	{
		ft_swap(&d->sa[i], &d->sa[i + 1]);
		i++;
	}
	write(1, "ra\n", 3);
}
