#include "pushswap.h"

int	ft_parse(char **argc, int arc)
{
	int	i;

	i = 0;
	while (i < arc)
	{
		if (argc[i][0] == '\0')
		{
			return (-1);
		}
		i++;
	}
	return (0);
}
