#include "pushswap.h"

void	ft_pb(t_list *d)
{
	int	i;

	i = d->sib;
	d->sia = d->sia - 1;
	d->sib = d->sib + 1;
	while (i > 0)
	{
		ft_swap(&d->sb[i], &d->sb[i - 1]);
		i--;
	}
	d->sb[0] = d->sa[0];
	i = 0;
	while (i <= d->sia - 1)
	{
		d->sa[i] = d->sa[i + 1];
		i++;
	}
	write(1, "pb\n", 3);
}
