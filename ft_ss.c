#include "pushswap.h"

void	ft_ss(t_list *d)
{
	int	temp;

	temp = d->sa[0];
	d->sa[0] = d->sa[1];
	d->sa[1] = temp;
	temp = d->sb[0];
	d->sb[0] = d->sb[1];
	d->sb[1] = temp;
	write(1, "ss\n", 3);
}
