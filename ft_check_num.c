#include "pushswap.h"

int	ft_check_num(char **args, int arc)
{
	int	i;

	i = -1;
	while (++i < arc)
	{
		if (ft_check(args[i + 1]) == -1 || *args[i + 1] == '\0')
		{
			return (-1);
		}
	}
	return (0);
}
